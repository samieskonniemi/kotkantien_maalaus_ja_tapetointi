//Button text bold on mouse enter, back to normal on mouse leave
$(document).ready(function(){
    $(".colorButtons").mouseenter(function(){
        $(this).css("font-weight", "bold");
    });
    $(".colorButtons").mouseleave(function(){
        $(this).css("font-weight", "normal");
    });
});

//Shadow over color buttons on mouse enter, off on mouse leave
$(document).ready(function(){
    $(".colorButtons").mouseenter(function(){
        $(this).css("box-shadow", "0px 0px 20px #FFFFFF");
    });
    $(".colorButtons").mouseleave(function(){
        $(this).css("box-shadow", "none");
    });
});

//Color info text (color name + HEX code) visible when clicked, left side
